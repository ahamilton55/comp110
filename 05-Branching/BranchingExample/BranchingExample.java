public class BranchingExample {
    public static void main(String[] args) {
        if (400 > 3) {
            System.out.println("400 is greater than 3");
        } else {
            System.out.println("400 is not greater than 3");
        }

        System.out.println(); // Justs adds a line break in the output

        if (400 < 3) {
            System.out.println("400 is less than 3");
        } else {
            System.out.println("400 is not less than 3");
        }

        if (3 > 4) {
            System.out.println("4 is greather than 3");
            System.out.println("Hello");
        }

    }
}