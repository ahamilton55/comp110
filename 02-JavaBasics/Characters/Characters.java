public class Characters {
    public static void main(String[] args) {
        char A = 'A';
        char B = 'B';
        char a = 'A' + 32;
        char newline = '\n';

        System.out.println(A);
        System.out.println(B);
        System.out.println(A+B);
        System.out.println(a);
        System.out.print(A);
        System.out.print(a + " " + newline);

        System.out.println(2 + Math.pow(2, 3));
    }
}